-- Tabela de permissões
CREATE TABLE SGI_PERMISSAO (
    PERM_ID NUMBER GENERATED BY DEFAULT AS IDENTITY INCREMENT BY 1 NOCACHE PRIMARY KEY,
    PERM_NOME VARCHAR2(50) NOT NULL,
    PERM_DESCRICAO VARCHAR2(255)
);

-- Associação usuário-permissão
CREATE TABLE SGI_USUARIO_PERMISSAO (
    USU_ID NUMBER NOT NULL,
    PERM_ID NUMBER NOT NULL,
    CONSTRAINT PK_SGI_USUARIO_PERMISSAO PRIMARY KEY (USU_ID, PERM_ID),
    CONSTRAINT FK_USUARIO_PERMISSAO_USUARIO FOREIGN KEY (USU_ID) REFERENCES SGI_USUARIOS(USU_ID),
    CONSTRAINT FK_USUARIO_PERMISSAO_PERMISSAO FOREIGN KEY (PERM_ID) REFERENCES SGI_PERMISSAO(PERM_ID)
);
